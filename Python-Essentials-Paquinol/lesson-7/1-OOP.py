# OOP means Object-Oriented Programming
# It is a programming paradigm that uses "objects" to design software.
# It is used to structure a program into simple, reusable pieces of code blueprints (usually called classes), which are used to create individual instances of objects.
# OOP focuses on the objects that developers want to manipulate rather than the logic required to manipulate them.
# This approach to programming is well-suited for programs that are large, complex, and actively updated or maintained.
# It also helps to keep the code DRY "Don't Repeat Yourself", and makes the code easier to maintain, modify, and debug.
# The four pillars of OOP are:
# 1. Encapsulation
# 2. Abstraction
# 3. Inheritance
# 4. Polymorphism

# Class is a blueprint for creating objects.
# It defines a set of attributes and methods that the created objects will have.
# To define a class, we use the "class" keyword followed by the class name and a colon.
# syntax:
# class ClassName:
#     # class body
# and so on just check the classes.py file
# Object is an instance of a class.
# It is created using the class constructor
# To create an object, we call the class as if it were a function, passing any arguments that the __init__ method requires.
# syntax:
# object_name = ClassName(arguments)
# and so on just check the classes.py file
# The __init__ method is a special method that is called when an object is created.
# It is used to initialize the attributes of the object.
# The self parameter is a reference to the current instance of the class.
# It is used to access the attributes and methods of the class in Python.